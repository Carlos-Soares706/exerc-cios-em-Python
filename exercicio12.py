"""
Exercício 12: Filtrando Nomes por Letra Inicial
Enunciado: Dada uma lista de nomes, escreva um programa que filtre e exiba apenas os nomes que começam com a letra 'A'.

nomes = ["Ana", "Bruno", "Amanda", "Carlos", "Alberto", "Beatriz"]
"""

nomes = ["Ana", "Bruno", "Amanda", "Carlos", "Alberto", "Beatriz"]

for nome in nomes:
    if nome [0] == ("A"):
        print(nome)